#include "address.hpp"


//----------------------------------------------------------------------------------------------------------------
/// constructor
//----------------------------------------------------------------------------------------------------------------
Address::Address()
{
}

//----------------------------------------------------------------------------------------------------------------
/// destructor
//----------------------------------------------------------------------------------------------------------------
Address::~Address()
{
    fields.empty();
}

//----------------------------------------------------------------------------------------------------------------
/// addField
//----------------------------------------------------------------------------------------------------------------
void Address::addField(fieldtype type, string value)
{
    Field temp;
    temp.type = type;
    temp.info = value;
    fields.put(temp);
}

//----------------------------------------------------------------------------------------------------------------
/// rmField
//----------------------------------------------------------------------------------------------------------------
bool Address::rmField(fieldtype type, string value)
{
    Field temp;
    temp.type = type;
    temp.info = value;
    fields.del(temp);
}

//----------------------------------------------------------------------------------------------------------------
/// find
//----------------------------------------------------------------------------------------------------------------
bool Address::find(Field* fld, fieldtype type)
{
    //return value
    bool rc;

    Field tmp;
    fields.reset();
    while( fields.next(tmp) == true)
    {
        if(tmp.type == type)
        {
            rc = true;
            fld = &tmp;
            break;
        }
    }

    return rc;
}
