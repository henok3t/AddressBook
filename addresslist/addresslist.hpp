#ifndef ADDRESSLIST_HPP
#define ADDRESSLIST_HPP

#include "linkedlist.hpp"
#include "address.hpp"

class AddressList
{
public:

    //----------------------------------------------------------------------------------------------------------------
    /// constructor
    //----------------------------------------------------------------------------------------------------------------
    AddressList();

    //----------------------------------------------------------------------------------------------------------------
    /// destructor
    //----------------------------------------------------------------------------------------------------------------
    ~AddressList();

    //----------------------------------------------------------------------------------------------------------------
    /// @brief
    /// reads addresses from a file
    /// @param input
    /// stream to read from
    //----------------------------------------------------------------------------------------------------------------
    void fread(ifstream input);

    //----------------------------------------------------------------------------------------------------------------
    /// @brief
    /// writes addresses to file
    /// @param output
    /// stream to write to
    //----------------------------------------------------------------------------------------------------------------
    void fwrite(ofstream output);

    //----------------------------------------------------------------------------------------------------------------
    /// @brief
    /// adds an address from user through standard input
    //----------------------------------------------------------------------------------------------------------------
    void addAddress();

    //----------------------------------------------------------------------------------------------------------------
    /// @brief
    /// edits the specified address
    /// @param firstname
    /// firstname for address
    /// @param lastname
    /// lastname for address
    //----------------------------------------------------------------------------------------------------------------
    void editAddress(string firstname, string lastname);

    //----------------------------------------------------------------------------------------------------------------
    /// @brief
    /// deletes the specified address
    /// @param firstname
    /// firstname for address
    /// @param lastname
    /// lastname for address
    //----------------------------------------------------------------------------------------------------------------
    void delAddress(string firstname, string lastname);

    //----------------------------------------------------------------------------------------------------------------
    /// @brief
    /// generates birthday cards for today
    //----------------------------------------------------------------------------------------------------------------
    void genBcards();

    //----------------------------------------------------------------------------------------------------------------
    /// @brief
    /// generates aniversary cards for today
    //----------------------------------------------------------------------------------------------------------------
    void genAcards();

private:

    LinkedList<Address> addressbook;
};

#endif // ADDRESSLIST_HPP
/// constructor
//----------------------------------------------------------------------------------------------------------------
AddressList::AddressList()
{/// constructor
    //----------------------------------------------------------------------------------------------------------------
    AddressList::AddressList()
    {
    }

    //----------------------------------------------------------------------------------------------------------------
    /// destructor
    //----------------------------------------------------------------------------------------------------------------
    AddressList::~AddressList()
    {
        addressbook.empty();
    }

}

//----------------------------------------------------------------------------------------------------------------
/// destructor
//----------------------------------------------------------------------------------------------------------------
AddressList::~AddressList()
{
    addressbook.empty();
}
